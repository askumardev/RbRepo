# A literal is any notation that lets you to represent a fixed value in source code. Below are literals in ruby
# nil is a way of expressing 'nothing'

str = "Hello World"     # string literal
int_val = 345           # integer literal
float_val = 3.75        # float literal
bool = true             # boolean literal
hash = {a: 1, b: 2}     # hash literal
arr = [1,2,3]           # array literal
:sym                    # symbol literal
x = nil                 # nil literal


puts "str=#{str}"
puts int_val
puts float_val
puts bool
puts hash
puts arr
puts :sym
puts "x=#{x}"

